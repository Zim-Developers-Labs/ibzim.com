export const getRankColor = (position: number) => {
  switch (position) {
    case 1:
      return 'border-amber-300 bg-gradient-to-r from-amber-50 to-yellow-50';
    case 2:
      return 'border-gray-300 bg-gradient-to-r from-gray-50 to-gray-100';
    case 3:
      return 'border-amber-400 bg-gradient-to-r from-amber-100 to-orange-50';
    default:
      return 'border-gray-200';
  }
};

export const getCurrentPeriod = (category: string) => {
  const currentMonth = new Date().getMonth() + 1;
  const votingMonths = {
    company: 1, // January
    city: 3, // March
    movie: 5, // May
    comedy: 7, // July
    school: 9, // September
    music: 11, // November
  };
  const resultsMonths = {
    company: 2, // February
    city: 4, // April
    movie: 6, // June
    comedy: 8, // August
    school: 10, // October
    music: 12, // December
  };

  const votingMonth = votingMonths[category as keyof typeof votingMonths];
  const resultsMonth = resultsMonths[category as keyof typeof resultsMonths];

  if (currentMonth === votingMonth) return 'voting';
  if (currentMonth === resultsMonth) return 'results';
  if (currentMonth > resultsMonth) return 'completed';
  return 'upcoming';
};

